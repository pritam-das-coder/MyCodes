# Write a function that takes the input of three sides of Triangle ,and print whether it is equilateral,
# isosceles,Scalene
a=int(input('Enter the 1st side of triangle : '))
b=int(input('Enter the 2nd side of triangle : '))
c=int(input('Enter the 3rd side of triangle : '))

if a==b and b==c : print("Equilateral")
elif a==b or b==c or c==a : print("Isosceles")
else : print("Scalene")

